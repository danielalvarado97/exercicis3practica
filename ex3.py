# Demanem a l'usuari que introdueixi el primer número
numero1 = float(input("Introdueix el primer número: "))

# Demanem a l'usuari que introdueixi el segon número
numero2 = float(input("Introdueix el segon número: "))

# Calculem la suma
suma = numero1 + numero2

# Calculem la resta
resta = numero1 - numero2

# Calculem la multiplicació
multiplicacio = numero1 * numero2

# Calculem la divisió sencera
divisio_sencera = numero1 // numero2

# Calculem el mòdul
modul = numero1 % numero2

# Calculem la divisió real
divisio_real = numero1 / numero2

# Imprimim els resultats
print(f"Suma: {suma}")
print(f"Resta: {resta}")
print(f"Multiplicació: {multiplicacio}")
print(f"Divisió sencera: {divisio_sencera}")
print(f"Mòdul: {modul}")
print(f"Divisió real: {divisio_real}")